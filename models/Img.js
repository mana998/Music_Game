class Img {
    constructor (src, startRow, startColumn, rows, columns, speed) {
        //src img
        this.src = src;
        //start position on stylesheet
        this.startRow = startRow;
        this.startColumn = startColumn;
        //length of animation
        this.rows = rows;
        this.columns = columns;
        //animation speed
        this.speed = speed;
    }

}